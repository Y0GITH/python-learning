# A data analyst calculates a KPI using the formula:
#           (SALES - COST) X 100 
#    KPI = ---------------------- 
#                 COST

#  📘 What is KPI?
#     KPI = Key Performance Indicator
#     A KPI is a measurable value that shows how well          something is performing.      

sales = 10000
cost = 2000
KPI = ((sales - cost)*100) / cost
print(f'KPI IS {KPI}%')