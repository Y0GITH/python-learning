# A company’s revenue grows by 10% annually.
# If initial revenue is ₹50,000, calculate revenue after 3 years.
initial_revenue = 50000
growth_rate = 1.10
no_of_years = 3
final_revenue = initial_revenue * ( growth_rate ** no_of_years)
# print(f'final revenue after {no_of_years} years at 10% growth rate of initial revenue of ${initial_revenue} is ${round(final_revenue)}. ')
print(f'final revenue is {round(final_revenue)}')